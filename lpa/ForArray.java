package lpa;

public class ForArray {
    public static void main(String[] args) {
      String alunos [] = {"Felipe", "Jonas", "Laura", "Marcos"};
  
      for (int x = 0; x < alunos.length; x++) {
        System.out.println("O aluno no índice x= " + x + " é: " + alunos[x]);
      }
    }
  }